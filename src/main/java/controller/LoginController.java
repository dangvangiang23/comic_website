    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
          request.getRequestDispatcher("login.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        DAO dao = new DAO();
        if (username != null && password != null) {
            Account account = dao.findAccount(username, password);
            if (account == null) {
                request.setAttribute("message", "Login failed, your password or username is incorrect!");
                            request.getRequestDispatcher("login.jsp").forward(request, response);

            } else {
                request.setAttribute("message", "Login Successful!");
                HttpSession session = request.getSession();               
                session.setAttribute("userLogged", account);
                session.setAttribute("username", username);
                 String context = request.getContextPath();
                if (account.isRole()) {
                    //trang admin
                     response.sendRedirect(context + "/home");
                    //request.getRequestDispatcher("admin.jsp").forward(request, response);
                } else {
                    //request.getRequestDispatcher("/home").forward(request, response);
                    response.sendRedirect(context + "/home");
                   
 
                }
            }
        } else {
            request.setAttribute("message", "Login Failed");
            request.getRequestDispatcher("login.jsp").forward(request, response);
        }

    }

